package day14_Stringday2;

public class String_indexof {

	public static void main(String[] args) {
	//                 012345678910     17
		String name = "president George washinton";
		System.out.println(name.charAt(0));// returs characters at givenindex position
		System.out.println(name.indexOf('p'));// returns index position on the given characters
		System.out.println(name.indexOf('p'));
		System.out.println(name.indexOf('G'));
		System.out.println(name.indexOf('G'));
		System.out.println(name.indexOf('e'));
		
		

	}

}
